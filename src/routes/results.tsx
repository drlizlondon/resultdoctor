import { createFileRoute, Link } from "@tanstack/react-router";
import { useState, useEffect } from "react";

export const Route = createFileRoute("/results")({
  component: ResultsPage,
});

/* ─────────────────────────────────────────────────────────────
   EXPORTED TYPES (imported by pathway files)
───────────────────────────────────────────────────────────── */

export type UrgencyLevel =
  | "emergency"
  | "urgent_referral"
  | "urgent"
  | "routine_referral"
  | "primary_care"
  | "reassure"
  | "monitor";

export interface SynthesisFlag {
  label: string;
  value: number;
  unit: string;
  isAbnormal: boolean;
  interpretation: string;
}

export interface ReferralItem {
  specialty: string;
  timeframe: string;
  note?: string;
}

export interface MvpResult {
  urgency: UrgencyLevel;
  clinicianHeadline: string;
  patientHeadline: string;
  patientSummary: string;
  patientAsk: string;           // What patient should literally say to their doctor
  verbatim: string[];
  verbatimTitle?: string;
  referrals: ReferralItem[];
  reasoning: string;            // Engine's plain-English reasoning
  source: {
    organisation: string;
    document: string;
    version: string;
    flowsheetUrl: string;       // Link to the actual guideline flowsheet
  };
  pathwayId: string;
  pathwayTitle: string;
  resultsEntered: SynthesisFlag[];
  reviewDueDate?: string;
}

/* ─────────────────────────────────────────────────────────────
   URGENCY CONFIG
───────────────────────────────────────────────────────────── */

const URGENCY_CONFIG: Record<UrgencyLevel, {
  label: string; color: string; bg: string;
  borderColor: string; icon: string; textColor: string;
}> = {
  emergency:        { label: "EMERGENCY",               color: "#7f1d1d", bg: "#fef2f2", borderColor: "#dc2626", icon: "🚨", textColor: "#dc2626" },
  urgent_referral:  { label: "SPECIALIST REFERRAL",     color: "#7c2d12", bg: "#fff7ed", borderColor: "#ea580c", icon: "⚡", textColor: "#c2410c" },
  urgent:           { label: "URGENT",                  color: "#854d0e", bg: "#fefce8", borderColor: "#ca8a04", icon: "⚠️", textColor: "#a16207" },
  routine_referral: { label: "ROUTINE REFERRAL",        color: "#1e3a5f", bg: "#eff6ff", borderColor: "#3b82f6", icon: "📋", textColor: "#1d4ed8" },
  primary_care:     { label: "PRIMARY CARE",            color: "#14532d", bg: "#f0fdf4", borderColor: "#16a34a", icon: "🏥", textColor: "#15803d" },
  reassure:         { label: "REASSURE",                color: "#14532d", bg: "#f0fdf4", borderColor: "#22c55e", icon: "✅", textColor: "#16a34a" },
  monitor:          { label: "MONITOR",                 color: "#374151", bg: "#f9fafb", borderColor: "#6b7280", icon: "👁",  textColor: "#4b5563" },
};

/* ─────────────────────────────────────────────────────────────
   COMPONENT
───────────────────────────────────────────────────────────── */

type ViewMode = "clinician" | "patient";

export default function ResultsPage() {
  const [result, setResult] = useState<MvpResult | null>(null);
  const [error, setError] = useState("");
  const [viewMode, setViewMode] = useState<ViewMode>("clinician");
  const [patientCopied, setPatientCopied] = useState(false);

  useEffect(() => {
    try {
      const raw = sessionStorage.getItem("rd_result");
      if (!raw) { setError("No result found. Please run a pathway first."); return; }
      setResult(JSON.parse(raw));
    } catch {
      setError("Could not load result. Please try again.");
    }
  }, []);

  if (error || !result) {
    return (
      <div style={{ fontFamily: "system-ui, sans-serif", minHeight: "100vh", background: "#f7f6f2", display: "flex", alignItems: "center", justifyContent: "center", flexDirection: "column", gap: 20 }}>
        <p style={{ fontSize: 17, color: "#666" }}>{error || "Loading…"}</p>
        <Link to="/pathways" style={{ padding: "10px 24px", background: "#1a1a1a", color: "white", borderRadius: 100, textDecoration: "none", fontSize: 14 }}>
          ← Go to pathways
        </Link>
      </div>
    );
  }

  const uc = URGENCY_CONFIG[result.urgency] ?? URGENCY_CONFIG.monitor;
  const isPatient = viewMode === "patient";

  async function copyPatientAsk() {
    await navigator.clipboard.writeText(result!.patientAsk);
    setPatientCopied(true);
    setTimeout(() => setPatientCopied(false), 2000);
  }

  const emailBody = isPatient
    ? `${result.patientSummary}\n\nWhat to say to your doctor:\n"${result.patientAsk}"\n\n— Generated by ResultDoctor\nSource: ${result.source.organisation} · ${result.source.document} · ${result.source.version}\nFlowsheet: ${result.source.flowsheetUrl}`
    : `${result.clinicianHeadline}\n\n${result.verbatimTitle}:\n${result.verbatim.map((l) => "• " + l).join("\n")}\n\nEngine reasoning: ${result.reasoning}\n\n— Generated by ResultDoctor\nSource: ${result.source.organisation} · ${result.source.document} · ${result.source.version}\nFlowsheet: ${result.source.flowsheetUrl}`;

  return (
    <div style={{ fontFamily: "'DM Sans', 'Helvetica Neue', sans-serif", background: "#f7f6f2", minHeight: "100vh", color: "#1a1a1a" }}>
      <style>{`
        @import url('https://fonts.googleapis.com/css2?family=DM+Sans:wght@300;400;500;600&family=Instrument+Serif:ital@0;1&display=swap');
        * { box-sizing: border-box; margin: 0; padding: 0; }

        .rd-nav { position: sticky; top: 0; z-index: 100; background: rgba(247,246,242,0.95); backdrop-filter: blur(12px); border-bottom: 1px solid #e8e5de; }
        .rd-nav-inner { max-width: 760px; margin: 0 auto; padding: 0 24px; height: 60px; display: flex; align-items: center; justify-content: space-between; gap: 12px; }
        .rd-logo { font-family: 'Instrument Serif', serif; font-size: 20px; font-style: italic; color: #1a1a1a; text-decoration: none; }

        .rd-toggle { display: flex; background: #ede9e0; border-radius: 100px; padding: 3px; gap: 2px; }
        .rd-toggle-btn { padding: 6px 16px; border-radius: 100px; font-size: 13px; font-weight: 500; cursor: pointer; border: none; transition: all 0.18s; background: transparent; color: #777; }
        .rd-toggle-btn.active { background: white; color: #1a1a1a; box-shadow: 0 1px 4px rgba(0,0,0,0.10); }

        .rd-wrap { max-width: 760px; margin: 0 auto; padding: 36px 24px 80px; }

        .rd-results-row { display: flex; gap: 8px; flex-wrap: wrap; margin-bottom: 24px; }
        .rd-flag { display: inline-flex; flex-direction: column; gap: 3px; padding: 10px 14px; border-radius: 12px; }
        .rd-flag-label { font-size: 11px; font-weight: 700; letter-spacing: 0.06em; text-transform: uppercase; }
        .rd-flag-value { font-size: 20px; font-weight: 700; font-family: 'Instrument Serif', serif; line-height: 1; }
        .rd-flag-unit { font-size: 11px; font-weight: 500; }
        .rd-flag-interp { font-size: 12px; line-height: 1.4; margin-top: 2px; max-width: 160px; }
        .rd-flag-abnormal { background: #fee2e2; color: #991b1b; }
        .rd-flag-normal { background: #e8f5ee; color: #14532d; }

        .rd-urgency-card { border-radius: 16px; padding: 28px 32px; margin-bottom: 20px; border-left-width: 5px; border-left-style: solid; }
        .rd-urgency-badge { display: inline-flex; align-items: center; gap: 8px; margin-bottom: 14px; }
        .rd-urgency-icon { font-size: 24px; }
        .rd-urgency-label { font-size: 12px; font-weight: 800; letter-spacing: 0.08em; }

        .rd-card { background: white; border-radius: 16px; border: 1px solid #e8e5de; padding: 26px 28px; margin-bottom: 14px; }
        .rd-section-label { font-size: 10px; font-weight: 700; letter-spacing: 0.1em; text-transform: uppercase; color: #aaa; margin-bottom: 10px; }

        .rd-verbatim-line { font-size: 14px; color: #444; line-height: 1.7; padding: 7px 0 7px 14px; border-bottom: 1px solid #f3f0ea; position: relative; }
        .rd-verbatim-line::before { content: "•"; position: absolute; left: 0; color: #2d5a3e; font-weight: 700; }
        .rd-verbatim-line:last-child { border-bottom: none; }

        .rd-referral-row { display: flex; justify-content: space-between; align-items: center; padding: 12px 0; border-bottom: 1px solid #f3f0ea; gap: 12px; }
        .rd-referral-row:last-child { border-bottom: none; }
        .rd-timeframe { display: inline-block; padding: 3px 10px; border-radius: 6px; font-size: 12px; font-weight: 600; background: #fff7ed; color: #c2410c; white-space: nowrap; }

        .rd-patient-ask { background: #f9f8f5; border-radius: 12px; padding: 20px 22px; border-left: 3px solid #2d5a3e; }
        .rd-ask-quote { font-size: 15px; color: #1a1a1a; line-height: 1.65; font-style: italic; }

        .rd-flowsheet-link { display: inline-flex; align-items: center; gap: 6px; font-size: 13px; font-weight: 600; color: #2d5a3e; text-decoration: none; padding: 8px 16px; border-radius: 100px; border: 1.5px solid #2d5a3e; transition: all 0.15s; }
        .rd-flowsheet-link:hover { background: #2d5a3e; color: white; }

        .rd-reasoning { background: #f3f0ea; border-radius: 12px; padding: 16px 18px; }

        .rd-action-btn { padding: 8px 18px; border-radius: 100px; font-size: 13px; font-weight: 500; cursor: pointer; border: 1.5px solid #d0cdc4; background: white; color: #555; transition: all 0.15s; text-decoration: none; display: inline-block; }
        .rd-action-btn:hover { border-color: #1a1a1a; color: #1a1a1a; }

        @media (max-width: 600px) {
          .rd-urgency-card { padding: 20px 20px; }
          .rd-card { padding: 20px; }
          .rd-wrap { padding: 24px 16px 60px; }
        }
        @media print { .rd-nav { display: none !important; } .no-print { display: none !important; } }
      `}</style>

      {/* NAV */}
      <div className="rd-nav">
        <div className="rd-nav-inner">
          <a href="/" className="rd-logo">Result<span style={{ color: "#2d5a3e" }}>Doctor</span></a>

          {/* Clinician / Patient toggle — the key feature */}
          <div className="rd-toggle no-print">
            {(["clinician", "patient"] as ViewMode[]).map((m) => (
              <button key={m} onClick={() => setViewMode(m)}
                className={`rd-toggle-btn ${viewMode === m ? "active" : ""}`}>
                {m === "clinician" ? "👨‍⚕️ Clinician" : "🙋 Patient"}
              </button>
            ))}
          </div>

          <Link to="/pathway/anaemia" className="rd-action-btn no-print" style={{ fontSize: 12, padding: "6px 14px" }}>
            ← New result
          </Link>
        </div>
      </div>

      <div className="rd-wrap">

        {/* VIEW MODE BANNER */}
        <div style={{ marginBottom: 20, padding: "10px 16px", borderRadius: 10, background: isPatient ? "#eff6ff" : "#f0fdf4", border: `1px solid ${isPatient ? "#bfdbfe" : "#bbf7d0"}`, display: "flex", alignItems: "center", gap: 10 }}>
          <span style={{ fontSize: 16 }}>{isPatient ? "🙋" : "👨‍⚕️"}</span>
          <p style={{ fontSize: 13, color: isPatient ? "#1e40af" : "#14532d", fontWeight: 500 }}>
            {isPatient
              ? "Showing patient view — plain English summary and what to say to your doctor"
              : "Showing clinician view — verbatim guideline, referral actions, and engine reasoning"}
          </p>
        </div>

        {/* RESULTS ENTERED — flags with interpretation */}
        <div style={{ marginBottom: 24 }}>
          <p className="rd-section-label">Results entered</p>
          <div className="rd-results-row">
            {result.resultsEntered.map((f) => (
              <div key={f.label} className={`rd-flag ${f.isAbnormal ? "rd-flag-abnormal" : "rd-flag-normal"}`}>
                <span className="rd-flag-label">{f.label}</span>
                <span className="rd-flag-value">
                  {f.value} <span className="rd-flag-unit">{f.unit}</span>
                </span>
                <span className="rd-flag-interp">{f.interpretation}</span>
              </div>
            ))}
          </div>
        </div>

        {/* URGENCY CARD */}
        <div className="rd-urgency-card" style={{ background: uc.bg, borderLeftColor: uc.borderColor }}>
          <div className="rd-urgency-badge">
            <span className="rd-urgency-icon">{uc.icon}</span>
            <span className="rd-urgency-label" style={{ color: uc.textColor }}>{uc.label}</span>
          </div>
          <h1 style={{ fontFamily: "'Instrument Serif', serif", fontSize: 26, fontWeight: 400, lineHeight: 1.25, marginBottom: 8 }}>
            {isPatient ? result.patientHeadline : result.clinicianHeadline}
          </h1>
          {isPatient && (
            <p style={{ fontSize: 15, color: "#555", lineHeight: 1.65, marginBottom: 14 }}>
              {result.patientSummary}
            </p>
          )}
          <p style={{ fontSize: 13, color: "#999" }}>{result.pathwayTitle} · {result.source.organisation}</p>
        </div>

        {/* REFERRALS — clinician view */}
        {!isPatient && result.referrals.length > 0 && (
          <div className="rd-card">
            <p className="rd-section-label">Referral actions</p>
            {result.referrals.map((ref, i) => (
              <div key={i} className="rd-referral-row">
                <div>
                  <p style={{ fontWeight: 600, fontSize: 15 }}>{ref.specialty}</p>
                  {ref.note && <p style={{ fontSize: 13, color: "#888", marginTop: 3 }}>{ref.note}</p>}
                </div>
                <span className="rd-timeframe">{ref.timeframe}</span>
              </div>
            ))}
          </div>
        )}

        {/* PATIENT: What to say to your doctor */}
        {isPatient && (
          <div className="rd-card">
            <p className="rd-section-label">What to say to your doctor</p>
            <div className="rd-patient-ask" style={{ marginBottom: 14 }}>
              <p style={{ fontSize: 11, fontWeight: 700, letterSpacing: "0.06em", textTransform: "uppercase", color: "#888", marginBottom: 8 }}>
                You could say:
              </p>
              <p className="rd-ask-quote">"{result.patientAsk}"</p>
            </div>
            <div style={{ display: "flex", gap: 10, flexWrap: "wrap", alignItems: "center" }}>
              <button onClick={copyPatientAsk} className="rd-action-btn" style={{ fontSize: 12 }}>
                {patientCopied ? "✓ Copied!" : "Copy this text"}
              </button>
              <a href={`mailto:?subject=${encodeURIComponent("My blood test results — ResultDoctor")}&body=${encodeURIComponent(emailBody)}`}
                className="rd-action-btn" style={{ fontSize: 12 }}>
                📤 Share with GP
              </a>
            </div>
          </div>
        )}

        {/* VERBATIM GUIDELINE — both views, labelled differently */}
        <div className="rd-card">
          <p className="rd-section-label">
            {isPatient ? "What the NHS guideline says" : result.verbatimTitle ?? "Guideline extract"}
          </p>
          {isPatient && (
            <p style={{ fontSize: 13, color: "#888", marginBottom: 14, lineHeight: 1.5 }}>
              This is the exact wording from the NHS NW London guideline your doctor will be following:
            </p>
          )}
          {result.verbatim.map((line, i) => (
            <div key={i} className="rd-verbatim-line">{line}</div>
          ))}
          <div style={{ marginTop: 18, paddingTop: 14, borderTop: "1px solid #f3f0ea", display: "flex", gap: 14, alignItems: "center", flexWrap: "wrap" }}>
            <p style={{ fontSize: 11, color: "#bbb" }}>
              {result.source.organisation} · {result.source.document} · {result.source.version}
            </p>
            <a href={result.source.flowsheetUrl} target="_blank" rel="noopener noreferrer"
              className="rd-flowsheet-link">
              View flowsheet ↗
            </a>
          </div>
        </div>

        {/* ENGINE REASONING — clinician view only */}
        {!isPatient && (
          <div className="rd-card">
            <p className="rd-section-label">Engine reasoning</p>
            <div className="rd-reasoning">
              <p style={{ fontSize: 14, color: "#444", lineHeight: 1.7 }}>{result.reasoning}</p>
            </div>
          </div>
        )}

        {/* PATIENT: Also see the flowsheet */}
        {isPatient && (
          <div style={{ display: "flex", gap: 12, alignItems: "center", flexWrap: "wrap", marginTop: 4 }}>
            <a href={result.source.flowsheetUrl} target="_blank" rel="noopener noreferrer"
              className="rd-flowsheet-link">
              View full NHS guideline flowsheet ↗
            </a>
            <p style={{ fontSize: 12, color: "#aaa" }}>Opens the official NWL ICB pathway PDF</p>
          </div>
        )}

        {/* ACTIONS */}
        <div style={{ marginTop: 28, paddingTop: 20, borderTop: "1px solid #e8e5de", display: "flex", gap: 10, flexWrap: "wrap" }} className="no-print">
          <button onClick={() => window.print()} className="rd-action-btn">🖨 Print / Save PDF</button>
          <a href={`mailto:?subject=${encodeURIComponent("ResultDoctor — " + result.pathwayTitle)}&body=${encodeURIComponent(emailBody)}`}
            className="rd-action-btn">📤 Email result</a>
          <Link to="/pathway/anaemia" className="rd-action-btn">🔄 New result</Link>
        </div>

        {/* DISCLAIMER */}
        <p style={{ marginTop: 32, fontSize: 12, color: "#bbb", textAlign: "center", lineHeight: 1.6 }}>
          This tool reproduces NHS NW London clinical guidelines verbatim and does not replace clinical judgement or a consultation with a qualified healthcare professional.
        </p>

      </div>
    </div>
  );
}
